-- 4. PERFORM INNER JOIN ON BOTH THE TABLE
SELECT C1.*, C2.*
FROM TABLE_1 AS C1 INNER JOIN TABLE_2 AS C2 ON C1.MAIN1_ID = C2.MAIN2_ID;


-- 5. PERFORM LEFT JOIN ON TABLE 1 WITH TABLE 2
SELECT C1.*, C2.*
FROM TABLE_1 AS C1 LEFT JOIN TABLE_2 AS C2 ON C1.MAIN1_ID = C2.MAIN2_ID;


-- 6. PERFORM RIGHT JOIN ON TABLE 2 WITH TABLE 1
SELECT C1.*, C2.*
FROM TABLE_1 AS C1 RIGHT JOIN TABLE_2 AS C2 ON C1.MAIN1_ID = C2.MAIN2_ID;


-- 7. PERFORM FULL OUTER JOIN ON BOTH THE TABLE
    SELECT C1.*, C2.*
    FROM TABLE_1 AS C1 LEFT JOIN TABLE_2 AS C2 ON C1.MAIN1_ID = C2.MAIN2_ID
UNION
    SELECT C1.*, C2.*
    FROM TABLE_1 AS C1 RIGHT JOIN TABLE_2 AS C2 ON C1.MAIN1_ID = C2.MAIN2_ID;