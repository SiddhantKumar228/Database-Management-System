-- SOLUTIONS:

-- 1. CREATE A DATABASE
CREATE DATABASE DATABASE_2;

USE DATABASE_2;


-- 2. CREATE TWO TABLE USING VARIOUS DATATYPES
CREATE TABLE TABLE_1
(
    MAIN1_ID INT PRIMARY KEY NOT NULL UNIQUE,
    FIRST_NAME VARCHAR(100),
    LAST_NAME VARCHAR(100),
    DATE_OF_BIRTH DATE
);

CREATE TABLE TABLE_2
(
    MAIN2_ID INT PRIMARY KEY NOT NULL UNIQUE,
    CLASS VARCHAR(100),
    EMAIL VARCHAR(100),
    JOINING_DATE DATETIME
);


-- 3. INSERT VALUES IN BOTH THE TABLE
